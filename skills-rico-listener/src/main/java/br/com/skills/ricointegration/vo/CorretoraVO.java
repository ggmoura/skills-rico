package br.com.skills.ricointegration.vo;

public class CorretoraVO {

	private String id;
	private String nome;
	private Double abp;
	private Double asp;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Double getAbp() {
		return abp;
	}

	public void setAbp(Double abp) {
		this.abp = abp;
	}

	public Double getAsp() {
		return asp;
	}

	public void setAsp(Double asp) {
		this.asp = asp;
	}

}
